     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $newTodo = new Todo;
        $newTodo->task = $request->new_task;
        $newTodo->created_at = now();
        $newTodo->updated_at = now();
        $newTodo->save();

        return $newTodo;
    }

